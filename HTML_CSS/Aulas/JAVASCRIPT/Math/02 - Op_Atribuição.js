/*
    OPERADORES DE ATRIBUIÇÃO

    =  => Atribuição
    += => Atribuição de adição
    -= => Atribuição de subtração
    *= => Atribuição de multiplicação
    /= => Atribuição de divisão
    %= => Atribuição de resto da divisão
    **= => Atribuição de exponenciação
    ++ => Atribuição de incremento
    -- => Atribuição de decremento
    

 */

// Exemplos de Operadores de Atribuição

// Atribuição simples (=)
let numero = 10

// Atribuição de adição (+=)
numero += 5; // numero = numero + 5
console.log(numero); // 15

// Atribuição de subtração (-=)
numero -= 3; // numero = numero - 3
console.log(numero); // 12

// Atribuição de multiplicação (*=)
numero *= 2; // numero = numero * 2
console.log(numero); // 24

// Atribuição de divisão (/=)
numero /= 4; // numero = numero / 4
console.log(numero); // 6

// Atribuição de resto (%=)
numero %= 4; // numero = numero % 4
console.log(numero); // 2

// Atribuição de exponenciação (**=)
numero **= 3; // numero = numero ** 3
console.log(numero); // 8

// Incremento (++)
numero++; // numero = numero + 1
console.log(numero); // 9

// Decremento (--)
numero--; // numero = numero - 1
console.log(numero); // 8