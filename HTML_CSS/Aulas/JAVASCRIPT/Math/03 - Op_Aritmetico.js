/*
    OPERADORES ARITMÉTICOS

    +  => Adição
    -  => Subtração
    *  => Multiplicação
    /  => Divisão
    %  => Resto da divisão
    ** => Exponenciação
    ++ => Incremento
    -- => Decremento
*/

// Adição
let soma = 10 + 5;        // 15
let concatena = "10" + 5; // "105"

// Subtração
let subtracao = 20 - 8;   // 12

// Multiplicação
let multiplicacao = 4 * 3; // 12

// Divisão
let divisao = 15 / 3;     // 5

// Resto da divisão
let resto = 17 % 3;       // 2

// Exponenciação
let exponencial = 2 ** 3; // 8

// Incremento
let numero = 5;
numero++;                 // 6

// Decremento
let valor = 10;
valor--;                 // 9

// Exemplos práticos combinados
let calculo = (10 + 5) * 2;    // 30
let mediaNotas = (7 + 8 + 9) / 3; // 8
let potencia = 3 ** 2 + 1;     // 10
